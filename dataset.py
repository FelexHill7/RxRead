"""
dataset.py — Data loading and preprocessing for handwriting recognition.

Supports two data sources:
  1. GNHK — real handwriting (JSON polygon annotations + page images)
  2. Synthetic — generated word images (folder of PNGs + labels.json)

Both are pre-cached as tensors at init for I/O-free training.
"""

import os
import json
import cv2
import torch
from torch.utils.data import Dataset, ConcatDataset
from torchvision import transforms
from PIL import Image

# Characters the model can recognise (index 0 is reserved for the CTC blank)
CHARS = " !\"#&'()*+,-./0123456789:;?ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz"
char2idx = {c: i + 1 for i, c in enumerate(CHARS)}  # 0 reserved for CTC blank
idx2char = {i + 1: c for i, c in enumerate(CHARS)}

IMG_HEIGHT = 32
IMG_WIDTH = 128


def encode_text(text):
    """Convert a string to a list of integer indices, skipping unknown chars."""
    return [char2idx[c] for c in text if c in char2idx]


class GNHKDataset(Dataset):
    """
    PyTorch Dataset for the GNHK handwriting corpus.

    Each GNHK sample is a full-page image plus a JSON file that contains a
    flat list of word-level annotations with polygon coordinates.  This class
    walks the directory tree, crops every annotated word, and stores the
    (crop, text) pairs in memory so training is I/O-free after init.
    """

    def __init__(self, root_dir):
        # Base transform: deterministic, applied once at init and cached
        base_transform = transforms.Compose([
            transforms.Grayscale(),
            transforms.Resize((IMG_HEIGHT, IMG_WIDTH)),
            transforms.ToTensor(),                       # [0, 1] range
            transforms.Normalize((0.5,), (0.5,)),        # scale to [-1, 1]
        ])

        # Walk the full directory tree so we find JSONs inside sub-folders
        # (e.g. train_data/train/*.json, test_data/test/*.json)
        samples = []
        for dirpath, _, filenames in os.walk(root_dir):
            for fname in filenames:
                if not fname.endswith(".json"):
                    continue

                json_path = os.path.join(dirpath, fname)
                img_path = json_path.replace(".json", ".jpg")

                if not os.path.exists(img_path):
                    continue

                with open(json_path) as f:
                    data = json.load(f)

                image = cv2.imread(img_path)
                if image is None:
                    continue

                # GNHK annotations are a flat list of dicts, each with
                # "text" and "polygon" (x0,y0 … x3,y3 quadrilateral).
                for obj in data:
                    text = obj.get("text", "").strip()
                    if not text or text in ["###", ""]:
                        continue

                    polygon = obj.get("polygon", None)
                    if polygon is None:
                        continue

                    # Derive an axis-aligned bounding box from the polygon
                    xs = [polygon[k] for k in ("x0", "x1", "x2", "x3")]
                    ys = [polygon[k] for k in ("y0", "y1", "y2", "y3")]
                    x_min, x_max = max(min(xs), 0), max(xs)
                    y_min, y_max = max(min(ys), 0), max(ys)

                    crop = image[y_min:y_max, x_min:x_max]
                    if crop.size == 0:
                        continue

                    samples.append((crop, text))

        # Pre-cache all base tensors (grayscale + resize + toTensor)
        # Then free raw numpy crops to save RAM
        self.cached = []
        for crop, text in samples:
            pil_img = Image.fromarray(cv2.cvtColor(crop, cv2.COLOR_BGR2RGB))
            tensor = base_transform(pil_img)
            label = torch.tensor(encode_text(text), dtype=torch.long)
            self.cached.append((tensor, label, text))

    def __len__(self):
        return len(self.cached)

    def __getitem__(self, idx):
        return self.cached[idx]


class SyntheticDataset(Dataset):
    """
    PyTorch Dataset for synthetic handwriting images generated by trdg.

    Reads a folder of PNG images + a labels.json mapping filename → text.
    Pre-caches all tensors at init, same format as GNHKDataset.
    """

    def __init__(self, root_dir):
        base_transform = transforms.Compose([
            transforms.Grayscale(),
            transforms.Resize((IMG_HEIGHT, IMG_WIDTH)),
            transforms.ToTensor(),
            transforms.Normalize((0.5,), (0.5,)),
        ])

        labels_path = os.path.join(root_dir, "labels.json")
        if not os.path.exists(labels_path):
            self.cached = []
            return

        with open(labels_path) as f:
            labels = json.load(f)

        self.cached = []
        for fname, text in labels.items():
            img_path = os.path.join(root_dir, fname)
            if not os.path.exists(img_path):
                continue

            text = text.strip()
            if not text:
                continue

            # Filter to only chars the model knows
            encoded = encode_text(text)
            if not encoded:
                continue

            pil_img = Image.open(img_path).convert("RGB")
            tensor = base_transform(pil_img)
            label = torch.tensor(encoded, dtype=torch.long)
            self.cached.append((tensor, label, text))

    def __len__(self):
        return len(self.cached)

    def __getitem__(self, idx):
        return self.cached[idx]


def build_dataset(gnhk_dir, synthetic_dir=None):
    """Build a combined dataset from GNHK + optional synthetic data.

    Returns a single Dataset. If synthetic_dir doesn't exist or has no data,
    falls back to GNHK only.
    """
    gnhk = GNHKDataset(gnhk_dir)
    print(f"  GNHK samples: {len(gnhk)}")

    if synthetic_dir and os.path.isdir(synthetic_dir):
        syn = SyntheticDataset(synthetic_dir)
        if len(syn) > 0:
            print(f"  Synthetic samples: {len(syn)}")
            combined = ConcatDataset([gnhk, syn])
            print(f"  Total combined: {len(combined)}")
            return combined

    return gnhk